    <h2>1~100までの偶数の合計値を表示</h2>
    <hr>
    <?php
        //計算結果の変数を生成
        $sum=0;
        //1~100まで繰り返し計算させる
        for($new=1;$new<=100;$new++){
            if($new%2==0){
                $sum=$sum+$new;
            }
        }
        print("1～100までの偶数の合計値は".$sum);
    ?>
